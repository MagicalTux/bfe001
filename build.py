#!/usr/bin/env python3
"""Build script for bfe001.net — generates static HTML from Markdown content and Jinja2 templates."""

import re
import sys
from datetime import date, datetime, timezone
from email.utils import format_datetime
from pathlib import Path
from xml.sax.saxutils import escape as xml_escape

import markdown
import yaml
from jinja2 import Environment, FileSystemLoader
from markdown.extensions import Extension
from markdown.treeprocessors import Treeprocessor

ROOT = Path(__file__).parent
CONTENT_DIR = ROOT / "content"
TEMPLATE_DIR = ROOT / "templates"
OUTPUT_DIR = ROOT

DEFAULT_LANG = "en"
SITE_URL = "https://www.bfe001.net"
GENERATED_COMMENT = "<!-- Generated by build.py — edit content/ and templates/ instead -->\n"


def discover_languages():
    """Find all language directories under content/."""
    return sorted(d.name for d in CONTENT_DIR.iterdir() if d.is_dir() and (d / "strings.yaml").exists())


def load_yaml(path):
    with open(path, "r", encoding="utf-8") as f:
        return yaml.safe_load(f)


def load_markdown_file(path):
    """Load a markdown file, return (frontmatter_dict, body_text)."""
    with open(path, "r", encoding="utf-8") as f:
        content = f.read()
    if content.startswith("---"):
        _, fm, body = content.split("---", 2)
        meta = yaml.safe_load(fm) or {}
        return meta, body.strip()
    return {}, content.strip()


def render_markdown(text):
    """Convert Markdown text to HTML."""
    md = markdown.Markdown(extensions=["extra"])
    return md.convert(text)


class MarkExternalLinksProcessor(Treeprocessor):
    """Open http(s) links that aren't on bfe001.net in a new tab."""

    def run(self, root):
        for a in root.iter("a"):
            href = a.get("href", "")
            if href.startswith(("http://", "https://")) and "bfe001.net" not in href:
                a.set("target", "_blank")
                a.set("rel", "noopener noreferrer")


class MarkExternalLinksExtension(Extension):
    def extendMarkdown(self, md):
        md.treeprocessors.register(MarkExternalLinksProcessor(md), "mark_external_links", 15)


def render_markdown_blog(text):
    """Markdown → HTML with external links tagged to open in a new tab."""
    md = markdown.Markdown(extensions=["extra", MarkExternalLinksExtension()])
    return md.convert(text)


def split_sections(body):
    """Split page body into sections by <!-- section: $ADDR "Title" --> comments."""
    pattern = r'<!--\s*section:\s*(\$\S+)\s+"([^"]+)"\s*-->'
    parts = re.split(pattern, body)

    sections = []
    # parts[0] is content before any section marker (usually empty)
    i = 1
    while i + 2 <= len(parts):
        addr = parts[i]
        title = parts[i + 1]
        content = parts[i + 2] if i + 2 < len(parts) else ""
        sections.append(
            {"addr": addr, "title": title, "content": render_markdown(content.strip())}
        )
        i += 3
    return sections


def load_timeline_entries(timeline_dir):
    """Load timeline entries from individual .md files, sorted by filename.
    Returns a list of sections, each with a 'name' and 'entries' list.
    Entries with a 'section' frontmatter field start a new section."""
    all_entries = []
    for path in sorted(timeline_dir.glob("[0-9]*.md")):
        meta, body = load_markdown_file(path)
        meta["content"] = render_markdown(body)
        all_entries.append(meta)

    # Group into sections
    sections = []
    current = {"name": None, "entries": []}
    for entry in all_entries:
        if entry.get("section"):
            if current["entries"]:
                sections.append(current)
            current = {"name": entry.pop("section"), "entries": [entry]}
        else:
            current["entries"].append(entry)
    if current["entries"]:
        sections.append(current)
    return sections


def page_url(output_path):
    """Compute the canonical URL for an output path."""
    rel = output_path.relative_to(ROOT).as_posix()
    return f"{SITE_URL}/{rel}"


def build_page(env, template_name, context, output_path):
    """Render a template and write to output."""
    template = env.get_template(template_name)
    url = page_url(output_path)
    context.setdefault("page_url", url)
    context.setdefault("site_url", SITE_URL)
    html = GENERATED_COMMENT + template.render(**context)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(html)
    print(f"  {output_path.relative_to(ROOT)}")


def build_amiga_subpages(env, lang, languages, common, out_dir, all_pages):
    """Build Amiga sub-pages from content/{lang}/amiga/*.md."""
    amiga_dir = CONTENT_DIR / lang / "amiga"
    if not amiga_dir.exists():
        return []

    # First pass: collect metadata
    subpages = []
    for path in sorted(amiga_dir.glob("*.md")):
        meta, body = load_markdown_file(path)
        slug = path.stem
        subpages.append({
            "slug": slug,
            "addr": meta.get("addr", ""),
            "title": meta.get("title", slug),
            "order": meta.get("order", 99),
            "path": f"amiga/{slug}.html",
        })

    # Sort by order field
    subpages.sort(key=lambda p: p["order"])

    # Second pass: render all pages
    for i, page in enumerate(subpages):
        path = amiga_dir / f"{page['slug']}.md"
        meta, body = load_markdown_file(path)
        sections = split_sections(body)
        prev_page = subpages[i - 1] if i > 0 else None
        next_page = subpages[i + 1] if i < len(subpages) - 1 else None

        # Sub-pages are one directory deeper
        sub_root = "../" + common["root"]

        page_id = f"amiga/{page['slug']}"
        build_page(
            env,
            "amiga_page.html",
            {
                **common,
                **meta,
                "root": sub_root,
                "nav_root": "../",
                "sections": sections,
                "active_page": "amiga",
                "active_page_path": page_id,
                "amiga_subpages": subpages,
                "prev_page": prev_page,
                "next_page": next_page,
                "hreflangs": hreflang_links(page_id, all_pages),
            },
            out_dir / "amiga" / f"{page['slug']}.html",
        )

    return subpages


def load_blog_posts(blog_dir):
    """Load blog post metadata from content/{lang}/blog/YYYYMMDD_slug.md files.
    Returns a list of {slug, date, title, description, body, filename} sorted by filename ascending (oldest first)."""
    posts = []
    for path in sorted(blog_dir.glob("[0-9]*.md")):
        meta, body = load_markdown_file(path)
        stem = path.stem
        date_prefix, _, filename_slug = stem.partition("_")
        slug = meta.get("slug") or filename_slug or stem
        date = meta.get("date") or f"{date_prefix[:4]}-{date_prefix[4:6]}-{date_prefix[6:8]}"
        posts.append({
            "slug": slug,
            "date": str(date),
            "title": meta.get("title", slug),
            "description": meta.get("description", ""),
            "body": body,
            "filename": stem,
        })
    return posts


def build_blog(env, lang, languages, common, out_dir, all_pages):
    """Build the blog index and individual post pages."""
    blog_dir = CONTENT_DIR / lang / "blog"
    if not blog_dir.exists():
        return

    intro_path = blog_dir / "_intro.md"
    intro_meta, intro_body = load_markdown_file(intro_path) if intro_path.exists() else ({}, "")

    posts = load_blog_posts(blog_dir)

    # Render each post (oldest first for prev/next ordering)
    for i, post in enumerate(posts):
        prev_page = posts[i - 1] if i > 0 else None  # older
        next_page = posts[i + 1] if i < len(posts) - 1 else None  # newer

        sub_root = "../" + common["root"]
        page_id = f"blog/{post['slug']}"

        build_page(
            env,
            "blog_post.html",
            {
                **common,
                "title": post["title"],
                "description": post["description"],
                "date": post["date"],
                "body": render_markdown_blog(post["body"]),
                "root": sub_root,
                "nav_root": "../",
                "active_page": "blog",
                "active_page_path": page_id,
                "prev_page": prev_page,
                "next_page": next_page,
                "hreflangs": hreflang_links(page_id, all_pages),
            },
            out_dir / "blog" / f"{post['slug']}.html",
        )

    # Render the index (newest first)
    index_posts = list(reversed(posts))
    build_page(
        env,
        "blog.html",
        {
            **common,
            **intro_meta,
            "intro": render_markdown_blog(intro_body),
            "posts": index_posts,
            "active_page": "blog",
            "hreflangs": hreflang_links("blog", all_pages),
        },
        out_dir / "blog.html",
    )


def hreflang_links(page_id, all_pages):
    """Return list of (lang, absolute_url) for hreflang tags."""
    lang_paths = all_pages.get(page_id, {})
    return [(l, f"{SITE_URL}/{p}") for l, p in lang_paths.items()]


def build_lang(env, lang, languages, all_pages):
    """Build all pages for a language."""
    lang_dir = CONTENT_DIR / lang
    strings = load_yaml(lang_dir / "strings.yaml")

    if lang == DEFAULT_LANG:
        out_dir = OUTPUT_DIR
        root = ""
    else:
        out_dir = OUTPUT_DIR / lang
        root = "../"

    common = {
        "lang": lang,
        "strings": strings,
        "root": root,
        "languages": languages,
        "current_lang": lang,
        "default_lang": DEFAULT_LANG,
    }

    # --- Index page ---
    meta, body = load_markdown_file(lang_dir / "index.md")
    sections = split_sections(body)
    build_page(
        env,
        "index.html",
        {**common, **meta, "sections": sections, "active_page": "index",
         "hreflangs": hreflang_links("index", all_pages)},
        out_dir / "index.html",
    )

    # --- Mind page ---
    meta, body = load_markdown_file(lang_dir / "mind.md")
    sections = split_sections(body)
    build_page(
        env,
        "page.html",
        {**common, **meta, "sections": sections, "active_page": "mind",
         "hreflangs": hreflang_links("mind", all_pages)},
        out_dir / "mind.html",
    )

    # --- Amiga sub-pages (build before main page to get subpage list) ---
    amiga_subpages = build_amiga_subpages(env, lang, languages, common, out_dir, all_pages)

    # --- Amiga page ---
    meta, body = load_markdown_file(lang_dir / "amiga.md")
    sections = split_sections(body)
    build_page(
        env,
        "page.html",
        {**common, **meta, "sections": sections, "active_page": "amiga",
         "amiga_subpages": amiga_subpages,
         "hreflangs": hreflang_links("amiga", all_pages)},
        out_dir / "amiga.html",
    )

    # --- Blog ---
    build_blog(env, lang, languages, common, out_dir, all_pages)

    # --- VVIQ page ---
    vviq2_md = lang_dir / "vviq2.md"
    if vviq2_md.exists():
        meta, body = load_markdown_file(vviq2_md)
        sections = split_sections(body)
        build_page(
            env,
            "page.html",
            {**common, **meta, "sections": sections, "active_page": "mind",
             "hreflangs": hreflang_links("vviq2", all_pages)},
            out_dir / "vviq2.html",
        )

    # --- Trial page ---
    trial_md = lang_dir / "trial.md"
    if trial_md.exists():
        meta, body = load_markdown_file(trial_md)
        sections = split_sections(body)
        build_page(
            env,
            "page.html",
            {**common, **meta, "sections": sections, "active_page": "timeline",
             "hreflangs": hreflang_links("trial", all_pages)},
            out_dir / "trial.html",
        )

    # --- Timeline page ---
    timeline_dir = lang_dir / "timeline"
    intro_meta, intro_body = load_markdown_file(timeline_dir / "_intro.md")
    timeline_sections = load_timeline_entries(timeline_dir)
    build_page(
        env,
        "timeline.html",
        {
            **common,
            **intro_meta,
            "intro": render_markdown(intro_body),
            "timeline_sections": timeline_sections,
            "active_page": "timeline",
            "hreflangs": hreflang_links("timeline", all_pages),
        },
        out_dir / "timeline.html",
    )


def post_datetime(post):
    """Return a timezone-aware datetime for a blog post.
    YAML parses `date: YYYY-MM-DD` as a datetime.date; we promote to 00:00 UTC."""
    d = post.get("date")
    if isinstance(d, datetime):
        return d if d.tzinfo else d.replace(tzinfo=timezone.utc)
    if isinstance(d, date):
        return datetime(d.year, d.month, d.day, tzinfo=timezone.utc)
    s = str(d)
    try:
        parsed = datetime.fromisoformat(s)
        return parsed if parsed.tzinfo else parsed.replace(tzinfo=timezone.utc)
    except ValueError:
        parts = s.split("-")
        return datetime(int(parts[0]), int(parts[1]), int(parts[2]), tzinfo=timezone.utc)


def post_feed_url(lang, slug):
    prefix = "" if lang == DEFAULT_LANG else f"{lang}/"
    return f"{SITE_URL}/{prefix}blog/{slug}.html"


def feed_channel_url(lang):
    prefix = "" if lang == DEFAULT_LANG else f"{lang}/"
    return f"{SITE_URL}/{prefix}blog.html"


def feed_self_url(lang, filename):
    prefix = "" if lang == DEFAULT_LANG else f"{lang}/"
    return f"{SITE_URL}/{prefix}{filename}"


def generate_rss(lang, strings, posts, out_path):
    """Generate an RSS 2.0 feed for one language's blog."""
    channel_title = strings.get("blog_nav", {}).get("back", "Log")
    channel_desc = "Short entries on open source, life, and whatever else."
    if lang == "ja":
        channel_desc = "オープンソース、日常、その他もろもろの短い記録。"
    channel_link = feed_channel_url(lang)
    self_link = feed_self_url(lang, "feed.xml")

    lines = ['<?xml version="1.0" encoding="UTF-8"?>']
    lines.append('<rss version="2.0"')
    lines.append('     xmlns:atom="http://www.w3.org/2005/Atom"')
    lines.append('     xmlns:content="http://purl.org/rss/1.0/modules/content/"')
    lines.append('     xmlns:dc="http://purl.org/dc/elements/1.1/">')
    lines.append("  <channel>")
    lines.append(f"    <title>BFE001 — {xml_escape(channel_title)}</title>")
    lines.append(f"    <link>{channel_link}</link>")
    lines.append(f'    <atom:link href="{self_link}" rel="self" type="application/rss+xml" />')
    lines.append(f"    <description>{xml_escape(channel_desc)}</description>")
    lines.append(f"    <language>{lang}</language>")

    if posts:
        last_build = format_datetime(post_datetime(posts[0]))
        lines.append(f"    <lastBuildDate>{last_build}</lastBuildDate>")

    for post in posts:
        url = post_feed_url(lang, post["slug"])
        pub_date = format_datetime(post_datetime(post))
        body_html = render_markdown_blog(post["body"])
        lines.append("    <item>")
        lines.append(f"      <title>{xml_escape(post['title'])}</title>")
        lines.append(f"      <link>{url}</link>")
        lines.append(f'      <guid isPermaLink="true">{url}</guid>')
        lines.append(f"      <pubDate>{pub_date}</pubDate>")
        lines.append("      <dc:creator>Mark Karpelès</dc:creator>")
        if post.get("description"):
            lines.append(f"      <description>{xml_escape(post['description'])}</description>")
        lines.append(f"      <content:encoded><![CDATA[{body_html}]]></content:encoded>")
        lines.append("    </item>")

    lines.append("  </channel>")
    lines.append("</rss>")

    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  {out_path.relative_to(ROOT)}")


def generate_atom(lang, strings, posts, out_path):
    """Generate an Atom 1.0 feed for one language's blog."""
    channel_title = strings.get("blog_nav", {}).get("back", "Log")
    channel_subtitle = "Short entries on open source, life, and whatever else."
    if lang == "ja":
        channel_subtitle = "オープンソース、日常、その他もろもろの短い記録。"
    channel_link = feed_channel_url(lang)
    self_link = feed_self_url(lang, "atom.xml")

    updated = format_atom_datetime(post_datetime(posts[0])) if posts else format_atom_datetime(datetime.now(timezone.utc))
    feed_id = channel_link

    lines = ['<?xml version="1.0" encoding="UTF-8"?>']
    lines.append(f'<feed xmlns="http://www.w3.org/2005/Atom" xml:lang="{lang}">')
    lines.append(f"  <title>BFE001 — {xml_escape(channel_title)}</title>")
    lines.append(f"  <subtitle>{xml_escape(channel_subtitle)}</subtitle>")
    lines.append(f'  <link rel="alternate" type="text/html" href="{channel_link}" />')
    lines.append(f'  <link rel="self" type="application/atom+xml" href="{self_link}" />')
    lines.append(f"  <id>{feed_id}</id>")
    lines.append(f"  <updated>{updated}</updated>")
    lines.append("  <author><name>Mark Karpelès</name></author>")

    for post in posts:
        url = post_feed_url(lang, post["slug"])
        published = format_atom_datetime(post_datetime(post))
        body_html = render_markdown_blog(post["body"])
        lines.append("  <entry>")
        lines.append(f"    <title>{xml_escape(post['title'])}</title>")
        lines.append(f'    <link rel="alternate" type="text/html" href="{url}" />')
        lines.append(f"    <id>{url}</id>")
        lines.append(f"    <published>{published}</published>")
        lines.append(f"    <updated>{published}</updated>")
        if post.get("description"):
            lines.append(f"    <summary>{xml_escape(post['description'])}</summary>")
        lines.append(f'    <content type="html"><![CDATA[{body_html}]]></content>')
        lines.append("  </entry>")

    lines.append("</feed>")

    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  {out_path.relative_to(ROOT)}")


def format_atom_datetime(dt):
    """Format a datetime in RFC 3339 (Atom-compatible)."""
    if dt.tzinfo is None:
        dt = dt.replace(tzinfo=timezone.utc)
    return dt.strftime("%Y-%m-%dT%H:%M:%S") + (
        "Z" if dt.utcoffset() == timezone.utc.utcoffset(dt) else dt.strftime("%z")
    )


def generate_news_sitemap(posts_by_lang, out_path):
    """Generate a Google News sitemap for blog posts from the last 48 hours.

    See https://developers.google.com/search/docs/crawling-indexing/sitemaps/news-sitemap
    An empty <urlset> is still valid if no posts are recent enough."""
    cutoff = datetime.now(timezone.utc).timestamp() - 2 * 24 * 60 * 60

    lines = ['<?xml version="1.0" encoding="UTF-8"?>']
    lines.append('<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"')
    lines.append('        xmlns:news="http://www.google.com/schemas/sitemap-news/0.9">')

    for lang, posts in posts_by_lang.items():
        for post in posts:
            dt = post_datetime(post)
            if dt.timestamp() < cutoff:
                continue
            url = post_feed_url(lang, post["slug"])
            pub_date = format_atom_datetime(dt)
            pub_name = "BFE001" if lang == DEFAULT_LANG else "BFE001 (日本語)"
            lines.append("  <url>")
            lines.append(f"    <loc>{url}</loc>")
            lines.append("    <news:news>")
            lines.append("      <news:publication>")
            lines.append(f"        <news:name>{xml_escape(pub_name)}</news:name>")
            lines.append(f"        <news:language>{lang}</news:language>")
            lines.append("      </news:publication>")
            lines.append(f"      <news:publication_date>{pub_date}</news:publication_date>")
            lines.append(f"      <news:title>{xml_escape(post['title'])}</news:title>")
            lines.append("    </news:news>")
            lines.append("  </url>")

    lines.append("</urlset>")

    with open(out_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  {out_path.relative_to(ROOT)}")


def generate_sitemap(pages):
    """Generate sitemap.xml from a list of canonical URLs."""
    lines = ['<?xml version="1.0" encoding="UTF-8"?>']
    lines.append('<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"')
    lines.append('        xmlns:xhtml="http://www.w3.org/1999/xhtml">')
    for url, alternates in pages:
        lines.append("  <url>")
        lines.append(f"    <loc>{url}</loc>")
        for alt_lang, alt_url in alternates:
            lines.append(f'    <xhtml:link rel="alternate" hreflang="{alt_lang}" href="{alt_url}" />')
        lines.append("  </url>")
    lines.append("</urlset>")
    out = ROOT / "sitemap.xml"
    with open(out, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  {out.relative_to(ROOT)}")


def generate_robots():
    """Generate robots.txt."""
    out = ROOT / "robots.txt"
    with open(out, "w", encoding="utf-8") as f:
        f.write(
            "User-agent: *\nAllow: /\n\n"
            f"Sitemap: {SITE_URL}/sitemap.xml\n"
            f"Sitemap: {SITE_URL}/news-sitemap.xml\n"
        )
    print(f"  {out.relative_to(ROOT)}")


def collect_page_paths(languages):
    """Collect all output paths per page across languages for hreflang/sitemap."""
    pages = {}  # key: page_id -> {lang: output_path_relative}

    for lang in languages:
        lang_dir = CONTENT_DIR / lang
        prefix = "" if lang == DEFAULT_LANG else f"{lang}/"

        # Main pages
        for page_name in ("index", "mind", "amiga", "vviq2", "trial", "blog", "timeline"):
            md = lang_dir / f"{page_name}.md"
            if page_name == "timeline":
                md = lang_dir / "timeline" / "_intro.md"
            elif page_name == "blog":
                md = lang_dir / "blog" / "_intro.md"
            if md.exists():
                pages.setdefault(page_name, {})[lang] = f"{prefix}{page_name}.html"

        # Amiga sub-pages
        amiga_dir = lang_dir / "amiga"
        if amiga_dir.exists():
            for path in sorted(amiga_dir.glob("*.md")):
                slug = path.stem
                page_id = f"amiga/{slug}"
                pages.setdefault(page_id, {})[lang] = f"{prefix}amiga/{slug}.html"

        # Blog post pages
        blog_dir = lang_dir / "blog"
        if blog_dir.exists():
            for path in sorted(blog_dir.glob("[0-9]*.md")):
                meta, _ = load_markdown_file(path)
                stem = path.stem
                _, _, filename_slug = stem.partition("_")
                slug = meta.get("slug") or filename_slug or stem
                page_id = f"blog/{slug}"
                pages.setdefault(page_id, {})[lang] = f"{prefix}blog/{slug}.html"

    return pages


def main():
    env = Environment(loader=FileSystemLoader(str(TEMPLATE_DIR)))
    languages = discover_languages()

    if not languages:
        print("No languages found in content/. Nothing to build.", file=sys.stderr)
        sys.exit(1)

    # Pre-compute hreflang mappings for all pages
    all_pages = collect_page_paths(languages)

    print("Building bfe001.net...")
    for lang in languages:
        print(f"\n[{lang}]")
        build_lang(env, lang, languages, all_pages)

    # Generate feeds (RSS + Atom) per language, using posts newest-first
    print("\n[feeds]")
    posts_by_lang = {}
    for lang in languages:
        blog_dir = CONTENT_DIR / lang / "blog"
        if not blog_dir.exists():
            continue
        strings = load_yaml(CONTENT_DIR / lang / "strings.yaml")
        posts = list(reversed(load_blog_posts(blog_dir)))  # newest first
        posts_by_lang[lang] = posts

        feed_root = OUTPUT_DIR if lang == DEFAULT_LANG else OUTPUT_DIR / lang
        generate_rss(lang, strings, posts, feed_root / "feed.xml")
        generate_atom(lang, strings, posts, feed_root / "atom.xml")

    # Generate sitemap, news sitemap, and robots.txt
    print("\n[seo]")
    sitemap_entries = []
    for page_id, lang_paths in all_pages.items():
        for lang, rel_path in lang_paths.items():
            url = f"{SITE_URL}/{rel_path}"
            alternates = [(l, f"{SITE_URL}/{p}") for l, p in lang_paths.items()]
            sitemap_entries.append((url, alternates))
    generate_sitemap(sitemap_entries)
    generate_news_sitemap(posts_by_lang, ROOT / "news-sitemap.xml")
    generate_robots()

